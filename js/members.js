// Ensure DATA and Utils are available
window.DATA = window.DATA || {};
window.Utils = window.Utils || {
    getLangText(obj, lang = 'he') {
        return obj[lang] || obj.en || '';
    }
};

// Populate DATA.members after DATA is defined
function initializeMembers() {
    if (window.DATA) {
        window.DATA.members = MEMBERS;
        window.MEMBERS = MEMBERS;
    }
}

// Function to create member cards
function createMemberCard(member) {
    const card = document.createElement('div');
    card.classList.add('member-card');
    card.setAttribute('data-member-id', member.id);
    
    const lang = document.documentElement.lang || 'he';
    
    card.innerHTML = `
        <div class="member-image">
            <img src="${member.image}" alt="${window.Utils.getLangText(member.name, lang)}">
        </div>
        <div class="member-info">
            <h3>${window.Utils.getLangText(member.name, lang)}</h3>
            <p>${window.Utils.getLangText(member.role, lang)}</p>
        </div>
    `;
    
    card.addEventListener('click', () => {
        window.location.href = `member.html?id=${member.id}`;
    });
    
    return card;
}

// Load members when the page loads
document.addEventListener('DOMContentLoaded', () => {
    // Initialize members data
    initializeMembers();
    
    // Check if we're on the index page
    if (!window.location.pathname.includes('index.html')) {
        console.log('Skipping members grid rendering');
        return;
    }
    
    const membersGrid = document.getElementById('members-grid');
    
    if (membersGrid) {
        // Render members if grid exists
        const membersToRender = window.DATA.members || [];
        
        if (membersToRender.length > 0) {
            membersToRender.forEach(member => {
                try {
                    membersGrid.appendChild(createMemberCard(member));
                } catch (error) {
                    console.error(`Error creating member card for ${member.name}: ${error.message}`);
                }
            });
        } else {
            console.warn('No members found to render');
        }
    } else {
        console.log('Members grid element not found');
    }
});
